package com.litemes.web.dto;

import jakarta.ws.rs.QueryParam;

/**
 * DTO for equipment model query parameters.
 * Supports fuzzy search by model code/name, exact filter by equipment type and status.
 */
public class EquipmentModelQueryDto {

    @QueryParam("modelCode")
    private String modelCode;

    @QueryParam("modelName")
    private String modelName;

    @QueryParam("equipmentTypeId")
    private Long equipmentTypeId;

    @QueryParam("status")
    private Integer status;

    @QueryParam("page")
    private Integer page = 1;

    @QueryParam("size")
    private Integer size = 10;

    public String getModelCode() {
        return modelCode;
    }

    public void setModelCode(String modelCode) {
        this.modelCode = modelCode;
    }

    public String getModelName() {
        return modelName;
    }

    public void setModelName(String modelName) {
        this.modelName = modelName;
    }

    public Long getEquipmentTypeId() {
        return equipmentTypeId;
    }

    public void setEquipmentTypeId(Long equipmentTypeId) {
        this.equipmentTypeId = equipmentTypeId;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public Integer getPage() {
        return page;
    }

    public void setPage(Integer page) {
        this.page = page;
    }

    public Integer getSize() {
        return size;
    }

    public void setSize(Integer size) {
        this.size = size;
    }
}
